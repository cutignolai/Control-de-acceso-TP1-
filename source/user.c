bool checkUser(char id[], char pass[])
{
	User_t all_users[] = {user1, user2, user3, user4, user5};
    bool answer = false;

    for(int i = 0; i < sizeof(all_users)/sizeof(all_users[0]); i++)
    {
        if ( (all_users[i].id == id) && (all_users[i].pass == pass) )
        {
            answer = true;
            break;
        }
        
    }

    return answer;

}




